//Splitting Component :-

//small components problem :-
//too many component
//confusing codebase
//too abstracted(creating something new  to hide the implementation details of that thing)

//large components problem :-
//Too many responsibility
//Might need too many props
//Hard to reuse
//Complex code, hard to understand

//recommended components solution :-
//logical separation of content / layout    :- does content or layout doesn't belong together
//reusability     :- is it possible to reuse the part of the component
//Responsibilities/complexity :-(single responsibility)  :- does component is doing too many thing ,confusing, complex or having too many props or state?
//Personal coding style (smaller or bigger component as you like) :-

//Component Categories :-
//Stateless / presentation components :-
//No State
//Can receive props  and simply received data or other content.
//usually small and reusable

//Stateful Components :-
//Have state
//Can still be reusable

//Structural Components :-
//"Page", "Layouts" or "screens" of the app
//Result of composition.
//Can be huge and non-reusable (but don't have to).

//prop drilling :-
//passing the props to several nested child component to get the data in deeply nested component.

//COMPONENT COMPOSITION :- (not tightly coupled as you pass or don't pass component as children)
//combining different components using the children prop (or explicitly defined props)
//it is done through children props.
//usecase :- create highly reusable and flexible components and Fix prop drilling.

// ELEMENT AS PROPS :-
//alternative of component composition

//Component props :-
//it is public API of the component

//PropsTypes :-
//specify the type of value the we expect from teh consumer of the component to pass in for each of props.
